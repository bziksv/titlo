type ChipTone = "emerald" | "sky" | "amber";

function chips(...items: { label: string; tone?: ChipTone }[]): ModuleV2Override["heroUi"] {
  const tones: ChipTone[] = ["emerald", "sky", "amber"];
  return {
    panelChips: items.map((c, i) => ({ label: c.label, tone: c.tone ?? tones[i % 3] })),
  };
}

